public class ifelse {
    public static void main(String args[]) {
        for (int i = 0; i < 5; i++) {
            switch (i) {
                case 1:
                    System.out.println("i is 1");
                    break;
                case 2:
                    System.out.println("i is 2");
                    break;
                case 3:
                    System.out.println("i is 3");
                    break;
                case 4:
                    System.out.println("i is 4");
                    break;

                default:
                    break;
            }
        }
    }

}
